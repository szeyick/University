package aps.elevator;

/**
 * The ElevatorDoorState.
 * <p>
 * This enumerator class is responsible for providing the states for the
 * elevator door.
 * <p>
 * @author szeyick
 * StudentID - 1763652.
 */
public enum ElevatorDoorState {
    
    /**
     * The elevator door is opened.
     */
    OPENED, 
    
    /**
    * The elevator door is closed.
    */
    CLOSED, 
    
    /**
     * The elevator door is opening.
     */ 
    OPENING,
    
    /**
     * The elevator door is closing.
     */
    CLOSING;
}
