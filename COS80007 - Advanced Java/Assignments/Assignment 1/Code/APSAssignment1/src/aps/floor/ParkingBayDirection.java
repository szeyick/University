package aps.floor;

/**
 * The {@link ParkingBayDirection}.
 * <p>
 * This enum represents the direction of the parking bay. It can either be a
 * north located parking bay, or south located parking bay.
 * <p>
 * @author szeyick
 */
public enum ParkingBayDirection {
    
    /**
     * The parking bay is located to the north of the centre aisle.
     */ 
    NORTH,
    
    /**
     * The parking bay is located to the south of the centre aisle.
     */
    SOUTH;
}
